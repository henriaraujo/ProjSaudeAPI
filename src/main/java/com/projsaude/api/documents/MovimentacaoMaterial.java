package com.projsaude.api.documents;

public class MovimentacaoMaterial {

	
	private String id;
	private String idMaterial;
	private String idQuantidade;
	private String data;
	private String status;
	private String idAlmoxarifado;
	private String idInstituicao;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIdMaterial() {
		return idMaterial;
	}
	public void setIdMaterial(String idMaterial) {
		this.idMaterial = idMaterial;
	}
	public String getIdQuantidade() {
		return idQuantidade;
	}
	public void setIdQuantidade(String idQuantidade) {
		this.idQuantidade = idQuantidade;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getIdAlmoxarifado() {
		return idAlmoxarifado;
	}
	public void setIdAlmoxarifado(String idAlmoxarifado) {
		this.idAlmoxarifado = idAlmoxarifado;
	}
	public String getIdInstituicao() {
		return idInstituicao;
	}
	public void setIdInstituicao(String idInstituicao) {
		this.idInstituicao = idInstituicao;
	}

	
	
	
}
